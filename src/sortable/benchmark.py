from copy import copy


def get_sort_algo():
    """Grab the correct sorting algorithm from the command line"""
    from sys import argv
    from importlib import import_module

    module_name, func_name = argv[1].rsplit(".", 1)
    module = import_module(module_name)
    return getattr(module, func_name)


def perftest(sort_algo):
    """Test the given sorting algorithm with our two tests lists"""
    sort_algo(copy(test_data_ints))
    sort_algo(copy(test_data_str), key=str.lower)


